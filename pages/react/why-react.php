  <p class="mb-6 text-black dark:text-gray-300">
    React has quickly become one of the most popular libraries for building user interfaces — and for good reason. Here's why developers and companies love using React in production.
  </p>

  <hr class="my-6 border-gray-300 dark:border-gray-700">

  <h2 class="text-2xl font-semibold mb-4 text-black dark:text-white">⚛️ 1. Component-Based Architecture</h2>
  <p class="mb-4 text-black dark:text-gray-300">
    React encourages building apps with small, reusable components. This modular approach improves code organization, testing, and scalability.
  </p>

  <hr class="my-6 border-gray-300 dark:border-gray-700">

  <h2 class="text-2xl font-semibold mb-4 text-black dark:text-white">⚡ 2. Fast with Virtual DOM</h2>
  <p class="mb-4 text-black dark:text-gray-300">
    React uses a Virtual DOM to update only the necessary parts of the UI. This makes rendering super fast, even for complex applications.
  </p>

  <hr class="my-6 border-gray-300 dark:border-gray-700">

  <h2 class="text-2xl font-semibold mb-4 text-black dark:text-white">🧠 3. Declarative Programming</h2>
  <p class="mb-4 text-black dark:text-gray-300">
    Instead of telling the browser how to update the UI, you describe what it should look like, and React handles the rest.
  </p>
  <pre class="bg-gray-100 dark:bg-gray-800 p-4 rounded text-sm mb-4 text-black dark:text-white overflow-x-auto"><code>// Declarative code
