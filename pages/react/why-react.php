  <hr class="my-6 border-gray-300 dark:border-gray-700">

  <h2 class="text-2xl font-semibold mb-4 text-black dark:text-white">🛠️ 4. Rich Ecosystem and Tools</h2>
  <ul class="list-disc list-inside mb-4 text-black dark:text-gray-300">
    <li>React Router for page navigation</li>
    <li>Redux, Zustand, or Context API for state management</li>
    <li>Next.js for server-side rendering</li>
    <li>Vite, Webpack for bundling</li>
    <li>Testing tools like Jest and React Testing Library</li>
  </ul>

  <hr class="my-6 border-gray-300 dark:border-gray-700">

  <h2 class="text-2xl font-semibold mb-4 text-black dark:text-white">🌎 5. Massive Community & Support</h2>
  <p class="mb-4 text-black dark:text-gray-300">
